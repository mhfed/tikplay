/** FNV-1a hash — same algorithm as Cover.tsx so the same seed always maps consistently. */
function hash(str: string): number {
  let h = 2166136261;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

/** Deterministically pick one artwork image for a given seed (playlist name, track title, ...). */
export function pickArt(seed: string): string {
  const idx = hash(seed || '♪') % ARTWORK.length;
  return ARTWORK[idx];
}

/** Pick a distinct artwork per index for a seed, so a list of cards never repeats the same photo back to back. */
export function pickArtAt(seed: string, offset: number): string {
  const idx = (hash(seed || '♪') + offset * 7) % ARTWORK.length;
  return ARTWORK[idx];
}
